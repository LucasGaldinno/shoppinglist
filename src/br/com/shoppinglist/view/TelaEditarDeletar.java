    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int id = Integer.parseInt(txtId.getText());
        ProdutoController prodC = new ProdutoController();
        try {
            prodC.ExcluirProduto(id);
        } catch (SQLException ex) {
            Logger.getLogger(TelaEditarDeletar.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        txtId.setText("");
        txtNomeProduto.setText("");
        spnQuantidade.setValue(0);
        spnValor.setValue(0);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int idProduto = Integer.parseInt(txtConsultar.getText());
        ProdutoController prodC = new ProdutoController();
        Produto prod = prodC.buscaPorId(idProduto);
        if (prod == null) {
            JOptionPane.showMessageDialog(this, "Produto n√£o encontrado!");
        } else {
            txtId.setText(String.valueOf(prod.getId()));
            txtNomeProduto.setText(prod.getNome());
            spnQuantidade.setValue(prod.getQtd());
            spnValor.setValue(prod.getValor());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        Produto prod = new Produto();

        try {
            int id;
            String nome;
            int qtd;
            double valor;
            
            id = Integer.parseInt(txtId.getText());
            nome = txtNomeProduto.getText();
            qtd = Integer.parseInt(spnQuantidade.getValue().toString());
            valor = Double.parseDouble(spnValor.getValue().toString());
            
            prod.setId(id);
            prod.setNome(nome);
            prod.setQtd(qtd);
            prod.setValor(valor);

            ProdutoController prodC = new ProdutoController();
            prodC.AlterarProduto(prod);

            JOptionPane.showMessageDialog(null, "Produto atualizado com sucesso!");

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

        txtId.setText("");
        txtNomeProduto.setText("");
        spnQuantidade.setValue(0);
        spnValor.setValue(0);
    }//GEN-LAST:event_btnEditarActionPerformed


    //Isso Ã© para carregar dados do banco na Tabela
    public void LerParaTabelaPorModelo(String nome) throws SQLException {

        DefaultTableModel model = (DefaultTableModel) tblListaProduto.getModel();
        model.setNumRows(0);

        ProdutoController prodC = new ProdutoController();
        
        for(Produto prod: prodC.buscaProduto(nome)){
            
            model.addRow(new Object[]{
            prod.getNome(),
            prod.getQtd(),
            prod.getValor()
        });
        }
        

    }

